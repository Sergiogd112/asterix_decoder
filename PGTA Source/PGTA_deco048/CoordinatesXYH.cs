namespace PGTA_deco048;

public class CoordinatesXYH : Coordinates
{
	private double x;

	private double y;

	private double height;

	public double X
	{
		get
		{
			return x;
		}
		set
		{
			x = value;
		}
	}

	public double Y
	{
		get
		{
			return y;
		}
		set
		{
			y = value;
		}
	}

	public double Height
	{
		get
		{
			return height;
		}
		set
		{
			height = value;
		}
	}
}
